package model;

public class Immobile {
    private String tipo;
    private int vani;
    private int costo;
    private int sup;
    private int anno;
    private int xidP;

    public String getTipo() {return tipo;}
    public void setTipo(String tipo) {this.tipo = tipo;}

    public int getVani() {return vani;}
    public void setVani(int vani) {this.vani = vani;}

    public int getCosto() {return costo;}
    public void setCosto(int costo) {this.costo = costo;}

    public int getSup() {return sup;}
    public void setSup(int sup) {this.sup = sup;}

    public int getAnno() {return anno;}
    public void setAnno(int anno) {this.anno = anno;}

    public int getXidP() {return xidP;}
    public void setXidP(int xidP) {this.xidP = xidP;}


}
